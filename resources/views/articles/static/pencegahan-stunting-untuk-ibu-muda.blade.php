  <x-static-article-content title="Cegah KEK" imgUrl="/images/articles/static/energy.svg">
    <p><strong>Cegah Kekurangan Energi Kronik (KEK)</strong> dengan mengkonsumsi makanan sesuai gizi seimbang. Kondisi KEK bisa dideteksi dini dengan melakukan pengukuran Lingkar Lengan Atas (LiLA). Minimal LiLA bagi ibu yang sehat adalah 23,5 cm.</p>
  </x-static-article-content>
  <x-static-article-content title="Persiapkan Persalinan" imgUrl="/images/articles/static/checklist.svg">
    <ul>
      <li>Tanyakan kepada bidan dan dokter tanggal perkiraan persalinan Pastikan suami atau keluarga mendampingi ibu saat periksa kehamilan</li>
      <li>Siapkan lebih dari 1 orang yang memiliki golongan darah yang sama dan bersedia menjadi pendonor jika diperlukan</li>
      <li>Persiapkan tabungan atau dana cadangan untuk biaya persalinan atau biaya lainnya</li>
      <li>Rencanakan melahirkan ditolong oleh dokter atau bidan di fasilitas kesehatan</li>
      <li>Siapkan dokumen-dokumen yang diperlukan untuk persiapan kelahiran bayi seperti KTP, Kartu Keluarga, dan Kartu Jaminan Kesehatan Nasional</li>
      <li>Rencanakan ikut Keluarga Berencana (KB) setelah bersalin dan tanyakan ke petugas kesehatan tentang cara ber-KB</li>
    </ul>
