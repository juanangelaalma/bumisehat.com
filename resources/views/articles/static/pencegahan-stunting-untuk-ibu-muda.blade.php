<x-static-article-section title="Pasca Kehamilan">
  <x-static-article-content title="Asi Eksklusif & Lanjutan" imgUrl="/images/articles/static/asi.svg">
    <ul>
      <li>Lakukan Inisiasi Menyusui Dini (IMD) segera setelah persalinan dan skin-to-skin contact minimal 1 jam</li>
      <li>Berikan ASI eksklusif kepada bayi dari usia 0 - 5 bulan dan teruskan hingga usia 2 tahun jika memungkinkan</li>
      <li>Berikan Makanan Pendamping ASI dari usia 6 - 23 bulan</li>
    </ul>
  </x-static-article-content>
  <x-static-article-content title="Mpasi dan Pemberian Makanan" imgUrl="/images/articles/static/baby-food.svg">
    <ul>
      <li>Banyak makan sayuran (250 gr/hari setara dengan 2,5 porsi sayur) dan buah-buahan (150 gr/hari setara dengan 3 buah pisang ambon ukuran sedang, 1,5 potong pepaya ukuran sedang, atau 3 buah jeruk ukuran sedang)</li>
      <li>Biasakan mengkonsumsi aneka ragam makanan pokok</li>
      <li>Batasi konsumsi pangan manis, asin, dan berlemak</li>
      <li>Biasakan sarapan</li>
      <li>Biasakan minum air putih yang cukup dan aman</li>
      <li>Biasakan membaca label pada kemasan pangan</li>
      <li>Kecukupan nilai kalori dan kandungan gizi makanan sesuai kebutuhan berdasarkan umur, bervariasi sesuai dgn ketersediaan lokal dan musim, dan disertai minum air bersih yang aman secukupnya</li>
    </ul>
  </x-static-article-content>
  <x-static-article-content title="Pemantauan Pertumbuhan" imgUrl="/images/articles/static/checklist.svg">
    <ul>
      <li>Identifikasi ukuran berat dan tinggi awal anak dari sejak lahir hingga usia 2 tahun untuk mengetahui kemajuan pertumbuhannya</li>
      <li>Bayi dikategorikan memiliki Berat Lahir Rendah (BBLR) apabila berat badannya kurang dari 2.500 gram (Kementerian Kesehatan)</li>
      <li>Perkembangan berat dan tinggi badan anak dapat dilakukan setiap bulan di posyandu atau fasilitas kesehatan lainnya dan hasilnya bisa dicatat di Kartu Menuju Sehat (KMS) yang tersedia di dalam buku Kesehatan Ibu dan Anak (KIA</li>
    </ul>
